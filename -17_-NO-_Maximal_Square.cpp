#include <bits/stdc++.h>
using namespace std;

int main() { cout << endl << endl << "######################################"<< endl << endl;

    for (int i = 0; i < 10; ++i) {
        for (int j = 0; j < 10; ++j) {
            cout << i << " " << j << " " << i*j << endl;
        }
    }


    cout << endl << "######################################";
    return 0;
}